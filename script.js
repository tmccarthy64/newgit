$(document).ready(function() {
	$('#reddit').mouseenter(function() { //increase opacity when mouse enters image
		$('#reddit').fadeTo('slow', '1.0');
	});
	$('#reddit').mouseleave(function() { //decrease to original opacity when mouse leaves image
		$('#reddit').fadeTo('slow', '0.5');
	});
});

